from __future__ import annotations

from pathlib import Path

PROMPTS_DIR = Path(__file__).resolve().parent / 'prompts'

STRICT_CHILD_SPAWN_POLICY_PROMPT = """默认在当前节点完成任务。派生子节点是严格例外，不是默认策略。

先按第一性原理收敛：先确认最直接的对象、事实、来源、关键矛盾，再验证派生必要性。
只有在你已读取核心材料或直接证据后，并且确认满足以下铁律中的至少一条时，才允许派生：
1. 问题极度开放，必须在执行中灵活探索旁支，例如需要多方面多角度的进行任务；
2. 存在两个以上冲突领域，必须隔离推理上下文，例如需要从正反面等对立点进行任务；
3. 需要沿多条独立路径并行推进，并行能显著提升效率，例如有多个明显需要逐一完成但是又相互独立的任务，并且每个任务工作量较大。
4. 自行完成一部分任务后，发现任务量明显高于预期，继续自行完成会显著降低效率。
若不满足以上任一条，禁止派生。
派生前必须明确：
1. 满足了哪条铁律以及原因；
2. 为什么现在选择子节点派生比当前节点独立完成要更好（上下文角度、效率角度等）；
3. 每个子节点的精确范围与交付物。
说不清以上三点，禁止派生。
除非任务明确要求发散性完成，否则超出任务要求的范围视为偏航，必须停止。
如果不确定是否应该派生，默认答案必须是：不派生，继续自行完成。"""


def load_prompt(name: str) -> str:
    return (PROMPTS_DIR / name).read_text(encoding='utf-8')
